#include "libft.h"

char	*ft_strdup(char *src)
{
	char			*dest;
	int				len;
	unsigned int	i;

	i = 0;
	len = ft_strlen(src);
	dest = malloc((len + 1) * sizeof(char));
	if (dest == NULL)
		return (NULL);
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
